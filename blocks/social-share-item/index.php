/**
 * Renders the `custom-blocks-templates/social-share-item` block on server.
 *
 * @param array $attributes The block attributes.
 *
 * @return string Rendered HTML of the referenced block.
 */
function render_cbt_social_share_item( $attributes ) {
	$service = ( isset( $attributes['service'] ) ) ? $attributes['service'] : 'Icon';
	$class_name = isset( $attributes['className'] ) ? ' ' . $attributes['className'] : false;
	$icon = cbt_get_icon( $service );

	$post_title = urlencode( get_the_title() );
	$post_url = urlencode( get_permalink() );
	$post_description = $post_title;
	$post_image = '';

	switch ($service) {
		case 'buffer':
			$url = 'https://buffer.com/add?url=' . $post_url  . '&title=' . $post_title . '&description=' . $post_description;
			break;
		case 'diaspora':
			$url = 'https://share.diasporafoundation.org/?title=' . $post_title . '&url=' . $post_url;
			break;
		case 'douban':
			$url = 'https://www.douban.com/share/service?name=' . $post_title . '&href=' . $post_url . '&image=' . $post_image . '&updated=&bm=&url=' . $post_url . '&title=' . $post_title . '&sel=' . $post_description;
			break;
		case 'evernote':
			$url = 'https://www.evernote.com/clip.action?url=' . $post_url . '&title=' . $post_title;
			break;
		case 'facebook':
			$url = 'https://www.facebook.com/sharer/sharer.php?u=' . $post_url . '&quote=';
			break;
		case 'flipboard':
			$url = 'https://share.flipboard.com/bookmarklet/popout?v=2&url=' . $post_url . '&title=' . $post_title;
			break;
		case 'hackernews':
			$url = 'https://news.ycombinator.com/submitlink?u=' . $post_url;
			break;
		case 'hatena':
			$url = 'https://b.hatena.ne.jp/entry/panel/?url=' . $post_url;
			break;
		case 'kakaostory':
			$url = 'https://story.kakao.com/share?url=' . $post_url;
			break;
		case 'line':
			$url = 'https://lineit.line.me/share/ui?url=' . $post_url . '&text=' . $post_title;
			break;
		case 'linkedin':
			$url = 'https://www.linkedin.com/shareArticle/?mini=true&url=' . $post_url . '&title=' . $post_title;
			break;
		case 'meneame':
			$url = 'http://meneame.net/login.php?return=/submit.php?url=' . $post_url;
			break;
		case 'mixi':
			$url = 'https://mixi.jp/share.pl?mode=login&u=' . $post_url;
			break;
		case 'pinterest':
			$url = 'https://pinterest.com/pin/create/link/?url=' . $post_url . '&media=' . $post_image . '&description=' . $post_title;
			break;
		case 'pocket':
			$url = 'https://getpocket.com/edit?url=' . $post_url;
			break;
		case 'qzone':
			$url = 'https://sns.qzone.qq.com/cgi-bin/qzshare/cgi_qzshare_onekey?url=' . $post_url;
			break;
		case 'reddit':
			$url = 'https://www.reddit.com/submit?url=' . $post_url . '&title=' . $post_title;
			break;
		case 'renren':
			$url = 'http://www.connect.renren.com/share/sharer?url=' . $post_url . '&title=' . $post_title;
			break;
		case 'telegram':
			$url = 'https://telegram.me/share/url?url=' . $post_url . '&text=' . $post_title;
			break;
		case 'tumblr':
			$url = 'https://www.tumblr.com/widgets/share/tool/preview?shareSource=legacy&canonicalUrl=&url=' . $post_url . '&posttype=link&title=' . $post_title . '&caption=&content=' . $post_url;
			break;
		case 'twitter':
			$url = 'https://twitter.com/intent/tweet?text=' . $post_title . ' - ' . $post_url;
			break;
		case 'vk':
			$url = 'http://vk.com/share.php?url=' . $post_url;
			break;
		case 'weibo':
			$url = 'http://service.weibo.com/share/share.php?url=' . $post_url . '&title=' . $post_title;
			break;
		case 'whatsapp':
			$url = 'whatsapp://send/?text=' . $post_title . ' - ' . $post_url;
			break;
		case 'xing':
			$url = 'https://www.xing.com/spi/shares/new?url=' . $post_url;
			break;
		default:
			$url = '#';
			break;
	}

	return '<li class="wp-social-share-item wp-social-share-item-' . esc_attr( $service ) . esc_attr( $class_name ) . '"><a href="' . esc_url( $url ) . '" aria-label="' . esc_attr( $service ) . '"> ' . $icon . '</a></li>';


}

